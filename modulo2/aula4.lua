x = 10
y = 20

print(x+y) -- Adição (10 + 20 = 30)
print(x-y) -- Subtração (10 - 20 = -10)
print(x*y) -- Multiplicação (10 * 20 = 200)
print(x/y) -- Divisão (10 / 20 = 0.5)
print(x^y) -- Potência (10^20 = 10 elevado a 20)
print(10+30) -- Pode ser feito diretamente (10 + 30 = 40)
print(10^(1/2)) -- Raiz quadrada de 10